#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*t;
	int		i;

	i = 0;
	t = (char *)malloc(ft_strlen(s) + 1);
	if (!t)
		return (NULL);
	while (s[i])
	{
		t[i] = s[i];
		i++;
	}
	t[i] = '\0';
	return (t);
}
